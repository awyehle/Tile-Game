import javax.swing.JButton;

public class Noah extends JButton{
		private int height;
		private int across;
		
		
	public Noah(int height,int across){
			this.height = height;
			this.across = across;
			
	}
	
	public int getHeight(){
		return height;
	}
	
	public int getAcross(){
		return across;
	}
	
	

}
